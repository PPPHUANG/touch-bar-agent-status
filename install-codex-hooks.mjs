#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const SCRIPT_DIR = path.dirname(new URL(import.meta.url).pathname);
const CONFIG_PATH = path.join(process.env.HOME || "/Users/ppphuang", ".codex", "config.toml");
const HOOK_SCRIPT = path.join(SCRIPT_DIR, "codex-touchbar-hook.mjs");
const CODEX_NODE = "/Applications/Codex.app/Contents/Resources/node";
const NODE_PATH = fs.existsSync(CODEX_NODE) ? CODEX_NODE : process.execPath;
const START = "# --- Codex Touch Bar buddy status hooks: start ---";
const END = "# --- Codex Touch Bar buddy status hooks: end ---";

function tomlSingleQuoted(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function hookBlock() {
  const command = `"${NODE_PATH}" "${HOOK_SCRIPT}"`;
  const commandToml = tomlSingleQuoted(command);
  const statusMessage = tomlSingleQuoted("Touch Bar: updating Codex buddy status");

  const events = [
    { name: "SessionStart", matcher: "startup|resume|clear|compact" },
    { name: "UserPromptSubmit" },
    { name: "PreToolUse" },
    { name: "PostToolUse" },
    { name: "PermissionRequest" },
    { name: "PreCompact" },
    { name: "PostCompact" },
    { name: "Stop" },
  ];

  const lines = [
    START,
    "# Generated by /Users/ppphuang/Documents/agent-status/install-codex-hooks.mjs",
    "# Run `codex` and open `/hooks` to review/trust these command hooks after changes.",
  ];

  for (const event of events) {
    lines.push("", `[[hooks.${event.name}]]`);
    if (event.matcher) {
      lines.push(`matcher = ${tomlSingleQuoted(event.matcher)}`);
    }
    lines.push(
      `[[hooks.${event.name}.hooks]]`,
      'type = "command"',
      `command = ${commandToml}`,
      "timeout = 5",
      `statusMessage = ${statusMessage}`,
    );
  }

  lines.push("", END, "");
  return lines.join("\n");
}

function removeExistingBlock(config) {
  const start = config.indexOf(START);
  if (start === -1) return config.trimEnd();

  const end = config.indexOf(END, start);
  if (end === -1) {
    throw new Error(`Found ${START} without matching ${END}`);
  }

  const afterEnd = end + END.length;
  return `${config.slice(0, start).trimEnd()}\n${config.slice(afterEnd).trimStart()}`.trimEnd();
}

function main() {
  if (!fs.existsSync(HOOK_SCRIPT)) {
    throw new Error(`Hook script not found: ${HOOK_SCRIPT}`);
  }

  const original = fs.readFileSync(CONFIG_PATH, "utf8");
  const backupPath = `${CONFIG_PATH}.bak.touchbar-${new Date()
    .toISOString()
    .replaceAll(/[:.]/g, "-")}`;
  fs.copyFileSync(CONFIG_PATH, backupPath);

  const next = `${removeExistingBlock(original)}\n\n${hookBlock()}`;
  fs.writeFileSync(CONFIG_PATH, next, "utf8");

  process.stdout.write(`Installed Codex Touch Bar hooks.\n`);
  process.stdout.write(`Config: ${CONFIG_PATH}\n`);
  process.stdout.write(`Backup: ${backupPath}\n`);
}

main();
